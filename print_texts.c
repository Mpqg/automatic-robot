#include "main.h"

/**
 * print_char - prints a character
 * This is for %c formatter
 * @return int
 */
int print_char(char c)
{
	if (c == NULL)
		return (0);
	_putchar(c);
}

/**
 * print_string - prints a string
 * This is for %s formatter
 * @return int
 */
int print_string(char *str)
{
	if (str == NULL)
		return (0);
}
